<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Initialize AOS animation library
        AOS.init({
            duration: 800,
            once: true
        });
        
        // Testimonial slider functionality
        const slider = document.getElementById('testimonials-slider');
        const prevBtn = document.getElementById('prev-testimonial');
        const nextBtn = document.getElementById('next-testimonial');
        const testimonials = slider.querySelectorAll('.testimonial-item');
        
        let currentIndex = 0;
        const maxIndex = testimonials.length - 1;
        
        // Set initial position
        updateSliderPosition();
        
        // Event listeners for controls
        prevBtn.addEventListener('click', function() {
            currentIndex = Math.max(0, currentIndex - 1);
            updateSliderPosition();
        });
        
        nextBtn.addEventListener('click', function() {
            currentIndex = Math.min(maxIndex, currentIndex + 1);
            updateSliderPosition();
        });
        
        function updateSliderPosition() {
            // Calculate the percentage to move based on viewport width
            let movePercentage = 100;
            
            if (window.innerWidth > 1200) {
                // On large screens, show 3 items at once
                movePercentage = 33.333;
            } else if (window.innerWidth > 992) {
                // On medium screens, show 2 items at once
                movePercentage = 50;
            }
            
            const translateValue = -currentIndex * movePercentage;
            slider.style.transform = `translateX(${translateValue}%)`;
            
            // Update button states
            prevBtn.disabled = currentIndex === 0;
            nextBtn.disabled = currentIndex === maxIndex;
            
            // Add active class to current testimonial
            testimonials.forEach((item, index) => {
                if (index === currentIndex) {
                    item.classList.add('active');
                } else {
                    item.classList.remove('active');
                }
            });
        }
        
        // Handle window resize to adjust slider
        window.addEventListener('resize', updateSliderPosition);
        
        // Counter animation for achievement numbers
        const achievementNumbers = document.querySelectorAll('.achievement-number');
        
        function animateCounter(element) {
            const target = parseInt(element.getAttribute('data-count'));
            const duration = 2000; // 2 seconds
            const step = target / (duration / 16); // 60fps
            let current = 0;
            
            const timer = setInterval(() => {
                current += step;
                if (current > target) {
                    element.textContent = target.toLocaleString();
                    clearInterval(timer);
                } else {
                    element.textContent = Math.floor(current).toLocaleString();
                }
            }, 16);
        }
        
        // Use Intersection Observer to trigger counter animation when in view
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateCounter(entry.target);
                    observer.unobserve(entry.target);
                }
            });
        }, { threshold: 0.5 });
        
        achievementNumbers.forEach(number => {
            observer.observe(number);
        });
    });
</script>

